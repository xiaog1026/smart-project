package jp.co.project.smart.model;
/**
 * CodeMasterVo
 * @author InnoX01
 *
 */
public class CodeMasterVo {
	private int codeId;
	private int wordId;
	private int codeNo;
	private String valuePhysicalName;
	private String valueLogicName;
	private String description;
	/**
	 * @return the codeId
	 */
	public int getCodeId() {
		return codeId;
	}
	/**
	 * @param codeId the codeId to set
	 */
	public void setCodeId(int codeId) {
		this.codeId = codeId;
	}
	/**
	 * @return the wordId
	 */
	public int getWordId() {
		return wordId;
	}
	/**
	 * @param wordId the wordId to set
	 */
	public void setWordId(int wordId) {
		this.wordId = wordId;
	}
	/**
	 * @return the codeNo
	 */
	public int getCodeNo() {
		return codeNo;
	}
	/**
	 * @param codeNo the codeNo to set
	 */
	public void setCodeNo(int codeNo) {
		this.codeNo = codeNo;
	}
	/**
	 * @return the valuePhysicalName
	 */
	public String getValuePhysicalName() {
		return valuePhysicalName;
	}
	/**
	 * @param valuePhysicalName the valuePhysicalName to set
	 */
	public void setValuePhysicalName(String valuePhysicalName) {
		this.valuePhysicalName = valuePhysicalName;
	}
	/**
	 * @return the valueLogicName
	 */
	public String getValueLogicName() {
		return valueLogicName;
	}
	/**
	 * @param valueLogicName the valueLogicName to set
	 */
	public void setValueLogicName(String valueLogicName) {
		this.valueLogicName = valueLogicName;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	
}
