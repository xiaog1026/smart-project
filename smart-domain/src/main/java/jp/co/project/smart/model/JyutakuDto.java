package jp.co.project.smart.model;

public class JyutakuDto {
	private int id;
	private String url;
	private String name;
	private String jyutakuNo;
	private String madori;
	private String menseki;
	private String shozaikai;
	private String shozaiti;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJyutakuNo() {
		return jyutakuNo;
	}
	public void setJyutakuNo(String jyutakuNo) {
		this.jyutakuNo = jyutakuNo;
	}
	public String getMadori() {
		return madori;
	}
	public void setMadori(String madori) {
		this.madori = madori;
	}
	public String getMenseki() {
		return menseki;
	}
	public void setMenseki(String menseki) {
		this.menseki = menseki;
	}
	public String getShozaikai() {
		return shozaikai;
	}
	public void setShozaikai(String shozaikai) {
		this.shozaikai = shozaikai;
	}
	public String getShozaiti() {
		return shozaiti;
	}
	public void setShozaiti(String shozaiti) {
		this.shozaiti = shozaiti;
	}
	
}
